package Q1_Basics;

public class PrintElementsOfArray {
    public static void main(String[] args) {
        int arr[] = {3, 5, 8, 9, 1};
        System.out.println("Elements of the array are:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
